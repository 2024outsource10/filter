import re

def page_apart(text):
    """
    根据 'Page' 和页码数字的格式分段文本，并在 'Page' 后添加冒号和换行符。

    参数:
        text (str): 输入的包含多个页内容的文本。

    返回:
        list: 分段后的文本列表，每个元素代表一个页面的内容。
    """
    # 使用正则表达式分割文本，匹配 'Page' 后跟随数字的格式
    pages = re.split(r'(Page\s*\d+)', text)
    result = []

    for i in range(1, len(pages), 2):
        # 在 'Page' 后面添加冒号和换行符
        page_header = pages[i] + ':\n'
        page_content = pages[i + 1]
        # 将处理后的页面内容加入结果列表
        result.append(page_header + page_content)

    return result
